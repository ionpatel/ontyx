export function getPurchaseOrderSummary() {
  const total = mockPurchaseOrders.length
  const draft = mockPurchaseOrders.filter(po => po.status === "draft").length
  const pending = mockPurchaseOrders.filter(po => po.status === "sent" || po.status === "confirmed").length
  const totalValue = mockPurchaseOrders.reduce((sum, po) => sum + po.total, 0)
  const outstanding = mockPurchaseOrders.reduce((sum, po) => sum + (po.total - po.amountPaid), 0)
  return { total, draft, pending, totalValue, outstanding }
}
